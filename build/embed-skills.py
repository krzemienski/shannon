#!/usr/bin/env python3
"""build/embed-skills.py — Shannon v1 agent embedding build step.

Two-part build:
  1. Copy `v1/shannon/skills/<skill>/` → `v1/shannon/agents/<agent>/_built/skills/<skill>/`
     (preserves disk layout for reference + Read-tool access at runtime)
  2. Inline the embedded SKILL.md bodies into `agents/<agent>/AGENT.md` so that
     Claude Code's agent loader (which reads AGENT.md as the agent's system prompt)
     gets the embedded content as part of the spawn context — without needing
     a separate `Skill: <name>` round-trip.

The inlining respects a sentinel pair:
    <!-- BEGIN EMBEDDED SKILLS — generated by build/embed-skills.py -->
    ...
    <!-- END EMBEDDED SKILLS -->

The build script tolerates an AGENT.md without sentinels by appending the
block at the end of the file. On re-run, it replaces the content between
sentinels (idempotent).

Idempotent: re-running overwrites `_built/` AND replaces the inlined block.

Iron Rule: refuses to silently substitute mock content. Missing skill → ERROR + exit 1.
No fallback paths. No placeholders.

Exit codes:
  0 — all agents built, all embedded skills resolved, AGENT.md inlined, evidence written
  1 — at least one agent has missing skill / empty manifest / unreadable manifest
"""

import os
import shutil
import sys
from pathlib import Path

try:
    import yaml
except ImportError:
    print("ERROR: PyYAML required. Install: pip install pyyaml", file=sys.stderr)
    sys.exit(1)

REPO_ROOT = Path(__file__).resolve().parent.parent
AGENTS_DIR = REPO_ROOT / "agents"
SKILLS_DIR = REPO_ROOT / "skills"
EVIDENCE_DIR = REPO_ROOT / ".planning" / "phases" / "02-agent-embedding" / "evidence"

BEGIN_SENTINEL = "<!-- BEGIN EMBEDDED SKILLS — generated by build/embed-skills.py -->"
END_SENTINEL = "<!-- END EMBEDDED SKILLS -->"


def render_embedded_block(agent_name: str, skill_names: list[str]) -> str:
    """Build the inlined SKILL.md block for an agent's AGENT.md.

    Each embedded skill contributes its SKILL.md body (frontmatter stripped to keep
    one frontmatter per AGENT.md). references/ are NOT inlined — they remain on disk
    in `_built/skills/<skill>/references/` for runtime Read-tool access.
    """
    parts = [
        BEGIN_SENTINEL,
        "",
        "# Embedded skill content (auto-generated)",
        "",
        f"The skills below are embedded in `{agent_name}`'s spawn context per Architecture C.",
        "Their canonical sources live at `v1/shannon/skills/<name>/`. Their `references/` directories are",
        f"available on disk at `agents/{agent_name}/_built/skills/<name>/references/` (read via the Read tool when needed).",
        "",
        "---",
        "",
    ]
    for skill in skill_names:
        skill_md_path = SKILLS_DIR / skill / "SKILL.md"
        if not skill_md_path.is_file():
            parts.append(f"## `{skill}` — MISSING ON DISK")
            parts.append("")
            continue
        body = skill_md_path.read_text()
        # Strip frontmatter (between first `---` and second `---`) to avoid YAML collision
        if body.startswith("---\n"):
            end = body.find("\n---\n", 4)
            if end != -1:
                body = body[end + 5:].lstrip()
        parts.append(f"## Embedded skill: `{skill}`")
        parts.append("")
        parts.append(body.rstrip())
        parts.append("")
        parts.append("---")
        parts.append("")
    parts.append(END_SENTINEL)
    return "\n".join(parts) + "\n"


def inline_into_agent_md(agent_dir: Path, embedded_block: str) -> None:
    """Replace content between sentinels, or append the block if no sentinels."""
    agent_md = agent_dir / "AGENT.md"
    text = agent_md.read_text()
    if BEGIN_SENTINEL in text and END_SENTINEL in text:
        start = text.index(BEGIN_SENTINEL)
        end = text.index(END_SENTINEL) + len(END_SENTINEL)
        # Replace including the trailing newline after END_SENTINEL if present
        if end < len(text) and text[end] == "\n":
            end += 1
        new_text = text[:start] + embedded_block + text[end:]
    else:
        # Append after a trailing newline
        if not text.endswith("\n"):
            text += "\n"
        new_text = text + "\n" + embedded_block
    agent_md.write_text(new_text)


def main() -> int:
    if not AGENTS_DIR.is_dir():
        print(f"ERROR: AGENTS_DIR not found: {AGENTS_DIR}", file=sys.stderr)
        return 1
    if not SKILLS_DIR.is_dir():
        print(f"ERROR: SKILLS_DIR not found: {SKILLS_DIR}", file=sys.stderr)
        return 1

    agent_dirs = sorted([d for d in AGENTS_DIR.iterdir() if d.is_dir() and not d.name.startswith(".")])
    if not agent_dirs:
        print("ERROR: no agent directories found", file=sys.stderr)
        return 1

    errors: list[str] = []
    summary: list[str] = []
    detail: list[str] = []

    for agent_dir in agent_dirs:
        manifest_path = agent_dir / "manifest.yml"
        if not manifest_path.is_file():
            errors.append(f"{agent_dir.name}: missing manifest.yml")
            continue

        try:
            manifest = yaml.safe_load(manifest_path.read_text())
        except yaml.YAMLError as e:
            errors.append(f"{agent_dir.name}: manifest.yml parse error: {e}")
            continue

        if not isinstance(manifest, dict):
            errors.append(f"{agent_dir.name}: manifest.yml is not a mapping")
            continue

        embedded = manifest.get("embedded_skills", [])
        if not embedded or not isinstance(embedded, list):
            errors.append(f"{agent_dir.name}: empty or non-list embedded_skills")
            continue

        built_dir = agent_dir / "_built" / "skills"
        # Idempotent: try to clear stale state but tolerate sandbox/perms blocking removal.
        # `dirs_exist_ok=True` in copytree then overwrites in place.
        if built_dir.exists():
            shutil.rmtree(built_dir, ignore_errors=True)
        built_dir.mkdir(parents=True, exist_ok=True)

        # Prune any leftover subdirs not in current manifest (best-effort, ignore_errors)
        declared_set = set(s for s in embedded if isinstance(s, str))
        for stale in built_dir.iterdir() if built_dir.exists() else []:
            if stale.name not in declared_set:
                shutil.rmtree(stale, ignore_errors=True)

        copied = 0
        for skill_name in embedded:
            if not isinstance(skill_name, str):
                errors.append(f"{agent_dir.name}: non-string skill entry {skill_name!r}")
                continue
            src = SKILLS_DIR / skill_name
            dst = built_dir / skill_name
            if not src.is_dir():
                errors.append(f"{agent_dir.name}: skill `{skill_name}` NOT FOUND in skills/ — Iron Rule violation, no substitution allowed")
                continue
            if not (src / "SKILL.md").is_file():
                errors.append(f"{agent_dir.name}: skill `{skill_name}` missing SKILL.md")
                continue
            shutil.copytree(
                src, dst,
                ignore=shutil.ignore_patterns(".DS_Store", "__pycache__"),
                dirs_exist_ok=True,
            )
            copied += 1
            detail.append(f"  {agent_dir.name} ← {skill_name}")

        # Inline the embedded SKILL.md bodies into AGENT.md so Claude Code's agent
        # loader actually sees the embedded content as part of the spawn context.
        valid_skills = [s for s in embedded if isinstance(s, str) and (SKILLS_DIR / s / "SKILL.md").is_file()]
        if valid_skills and (agent_dir / "AGENT.md").is_file():
            block = render_embedded_block(agent_dir.name, valid_skills)
            inline_into_agent_md(agent_dir, block)
            detail.append(f"  {agent_dir.name}: inlined {len(valid_skills)} SKILL.md bodies into AGENT.md")

        summary.append(f"{agent_dir.name}: embedded {copied}/{len(embedded)} (+inlined into AGENT.md)")

    print("=" * 60)
    print("Shannon v1 embed-skills build")
    print("=" * 60)
    for line in summary:
        print(line)
    print()
    if errors:
        print("ERRORS:")
        for e in errors:
            print(f"  ✗ {e}")
        return 1

    # Write evidence log
    EVIDENCE_DIR.mkdir(parents=True, exist_ok=True)
    log_path = EVIDENCE_DIR / "build-skills.log"
    log_path.write_text(
        "Shannon v1 embed-skills build evidence\n"
        + "=" * 60 + "\n\n"
        + "## Summary\n\n"
        + "\n".join(summary) + "\n\n"
        + "## Per-skill copies\n\n"
        + "\n".join(detail) + "\n"
    )
    print(f"\nBuild OK. Evidence: {log_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
